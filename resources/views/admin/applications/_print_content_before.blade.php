</p>
<p class="nm">
  <strong>第18条（分離可能性）</strong>
</p>
<p>
  本契約のいずれかの条項又はその一部が、管轄権を有する裁判所によって違法又は無効と判断された場合であっても、残りの条項については、その後も継続して有効に存続する。
</p>

<p class="nm">
  <strong>第19条 (準拠法・合意管轄)</strong>
</p>
<p>
